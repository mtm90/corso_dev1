// gli operatori sono utilizzati per eseguire operazioni su uno o più operandi


// operatori aritmetici


int a = 10;

int b = 20;

int somma = a + b;

int differenza = a - b;

int prodotto = a * b ;

int divisione = a / b;

int modulo = a % b;


// operatori di confronto 


int c = 30;

int c == a;             // false

int c != a;             // true

int c > a;              // true

int c < a;              // false

int c >= a;             // true

int c <= a;             // false


// operatori logici


bool x = true;

bool y = false;

// and
bool z = x && y;        // false

// or
bool w = x || y;        // true

// not
bool v = !x;            // false


